const dictionaries: Record<AppLocale, Record<TranslationKey, string>> = {
  'en-US': en,
  'zh-CN': zh,
};

function readLocale(): AppLocale {
  if (typeof localStorage === 'undefined') return 'zh-CN';
  const saved = localStorage.getItem(localeKey);
  return saved === 'en-US' || saved === 'zh-CN' ? saved : 'zh-CN';
}

export const useLocaleStore = defineStore('locale', () => {
  const current = ref<AppLocale>(readLocale());
  const isChinese = computed(() => current.value === 'zh-CN');

  function setLocale(locale: AppLocale) {
    current.value = locale;
    localStorage.setItem(localeKey, locale);
  }

  function toggleLocale() {
    setLocale(current.value === 'zh-CN' ? 'en-US' : 'zh-CN');
  }

  function t(key: TranslationKey, params: Record<string, string | number> = {}) {
    let value = dictionaries[current.value][key] ?? dictionaries['en-US'][key] ?? key;
    Object.entries(params).forEach(([paramKey, paramValue]) => {
      value = value.replaceAll(`{${paramKey}}`, String(paramValue));
    });
    return value;
  }

  return {
    current,
    isChinese,
    setLocale,
    toggleLocale,
    t,
  };
});
